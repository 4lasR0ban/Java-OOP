public class Lingkaran {
    // Luas
    int hitungLuas(int a){
        int Luas = a * a;
        return Luas;
    }
    
    double hitungLuas(double a){
        double Luas = a * a;
        return Luas;
    }
    
    // Keliling
    int hitungKeliling(int a){
        int Keliling = 2 * a;
        return Keliling;
    }
    
    double hitungKeliling(double a){
        double Keliling = 2 * a;
        return Keliling;
    }
}
